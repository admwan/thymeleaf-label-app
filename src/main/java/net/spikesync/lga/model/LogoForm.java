package net.spikesync.lga.model;

public class LogoForm {

    private String logoType;
    private String customText;

    // Constructors
    public LogoForm() {}

    // Getters and Setters
    public String getLogoType() { return logoType; }
    public void setLogoType(String logoType) { this.logoType = logoType; }

    public String getCustomText() { return customText; }
    public void setCustomText(String customText) { this.customText = customText; }
}
